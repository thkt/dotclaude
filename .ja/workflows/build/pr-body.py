#!/usr/bin/env python3
"""Usage: pr-body.py   (ship payload JSON on stdin)

build.js が既に保持している構造化データから、build workflow の draft-PR fact tail を
決定的に描画する。PR body は fail-closed な面であり、verify 結果、未解決の
critical/high 件数、「not re-audited」警告を常に載せる。agent は先頭の
"## Summary" (人間レビュアーの入口) だけを書き、この tail をその下に append する。

フォーマットは意図的に簡潔で markdown 構造 (自動生成ブロックを示す 1 行ラベル、
1 行の status、bold ラベル、bullet、失敗ログ用の折りたたみ <details>)。
safety-critical な事実は常に存在する status 行 / callout に置く。純粋に情報的な
リスト (assumptions, backlog, anomalies) は非空のときだけ出すので、clean run では
セクションごとに「None」を繰り返さず短いままになる。

2 方向に fail-closed。parse できない payload、または safety-critical key
(reaudited / tests_pass / gates_pass) を欠く payload は、それらしい「clean」body を
出さず stdout 空で exit 1 する。欠けた key は (呼び出し側の `&&` チェーンが PR を
中止することで) 表面化させ、安心させる値に default しない。

stdin:  JSON {issue, assumptions[], residual_blocking[], reaudited,
              code_anomalies[], tests_pass, gates_pass, verify_output,
              conformance[]}
stdout: markdown の fact tail。先頭は空行 + 水平線。
完了時は exit 0。parse error または必須 key の欠落時は exit 1。
"""

import json
import sys
from pathlib import Path

REQUIRED_KEYS = ("reaudited", "tests_pass", "gates_pass")

# body language ごとの人間向けラベル。翻訳するのは prose ラベルのみで、GitHub
# キーワード `Closes`、code-fence の status 行、`/issue` のようなコマンド名は auto-close
# と copy-paste が動くよう verbatim。未知の言語は英語に fallback する。tail を
# 決定的に保つため agent 提供でなくコード内に置く。
LABELS = {
    "english": {
        "tail_header": "_Auto-generated by the build workflow: verification result and open items for the reviewer._",
        "not_reaudited": "> **Not re-audited**. Final fix round unverified; blocking findings may remain.",
        "verify_output": "verify output",
        "assumptions": "Assumptions (veto targets)",
        "unresolved": "Unresolved critical/high",
        "conformance": "Issue conformance (review independently)",
        "anomalies": "Anomalies (Red unconfirmed)",
    },
    "japanese": {
        "tail_header": "_build workflow が自動生成。レビュアー向けの検証結果と未解決項目。_",
        "not_reaudited": "> **未再audit**。最終修正ラウンドが未検証。blocking findings が残っている可能性がある。",
        "verify_output": "verify 出力",
        "assumptions": "前提 (veto 対象)",
        "unresolved": "未解決 critical/high",
        "conformance": "Issue 適合性 (独立レビュー)",
        "anomalies": "異常 (Red 未確認)",
    },
}


def _default_language():
    """dotclaude settings から取得する PR body の言語。best-effort で、read/parse
    失敗時は英語に fallback し tail は描画され続ける。"""
    try:
        with open(Path.home() / ".claude" / "settings.json") as f:
            return json.load(f).get("language") or "english"
    except (OSError, json.JSONDecodeError):
        return "english"


def fail(message):
    print(f"Error: {message}", file=sys.stderr)
    sys.exit(1)


def _suffix(*parts):
    """存在する部分を ' (a, b)' として連結する。1 つも無ければ ''。"""
    kept = [str(p) for p in parts if p]
    return f" ({', '.join(kept)})" if kept else ""


def _list(items):
    return items if isinstance(items, list) else []


def _fence(text):
    """text 内の最長 backtick 連より 1 つ以上長い backtick fence。これにより、```
    を含む内容でも code block が途中で終わらない。"""
    longest = current = 0
    for ch in text:
        current = current + 1 if ch == "`" else 0
        longest = max(longest, current)
    return "`" * max(3, longest + 1)


def render(payload):
    issue = str(payload.get("issue", "")).strip()
    reaudited = bool(payload.get("reaudited", True))
    tests = "pass" if payload.get("tests_pass") else "FAIL"
    gates = "pass" if payload.get("gates_pass") else "FAIL"
    residual = _list(payload.get("residual_blocking"))
    lang = (payload.get("language") or "english").lower()
    L = LABELS.get(lang, LABELS["english"])

    out = [L["tail_header"], f"Closes #{issue}" if issue else "Closes #"]

    blocking = str(len(residual)) + ("" if reaudited else " (not re-audited)")
    out.append(f"`verify tests={tests} gates={gates}` · `blocking {blocking}`")

    if not reaudited:
        out.append(L["not_reaudited"])

    if tests == "FAIL" or gates == "FAIL":
        detail = payload.get("verify_output")
        if detail:
            body = detail if isinstance(detail, str) else json.dumps(detail, indent=2)
            fence = _fence(body)
            out.append(
                f"<details><summary>{L['verify_output']}</summary>\n\n{fence}\n{body}\n{fence}\n\n</details>"
            )

    def section(label, items, render_item):
        items = _list(items)
        if not items:
            return
        lines = []
        for x in items:
            try:
                text = render_item(x)
            except (AttributeError, TypeError, KeyError):
                # malformed (例: non-dict) な item は render を crash させて fail-closed
                # tail 全体を落としてはならない。代わりに raw string に degrade する。
                text = str(x)
            # 各 item を 1 行に保ち、埋め込まれた改行が list を壊したり次行を heading に
            # 昇格させたりしないようにする。
            lines.append("- " + " ".join(str(text).split("\n")))
        out.append(f"**{label}**\n" + "\n".join(lines))

    section(L["assumptions"], payload.get("assumptions"), str)
    section(
        L["unresolved"],
        residual,
        lambda f: (
            f"[{f.get('severity', '?')}] {f.get('summary', '')}".rstrip()
            + _suffix(f.get("file"))
        ),
    )
    section(
        L["conformance"],
        payload.get("conformance"),
        lambda f: (
            f"[{f.get('category', '?')}] {f.get('detail', '')}".rstrip()
            + _suffix(f.get("location"), f.get("spec_line"))
        ),
    )
    section(
        L["anomalies"],
        payload.get("code_anomalies"),
        lambda a: (
            f"{a.get('unit', '?')} ({a.get('kind', '?')}): {a.get('notes', '')}".rstrip()
        ),
    )

    # 空行 + 水平線で始め、agent の Summary の下に append (>>) したときこの machine tail
    # が分離を保ち、summary の最終行を setext heading に変えず、自動生成セクションの
    # 開始を示す。
    return "\n\n---\n\n" + "\n\n".join(out) + "\n"


def main():
    try:
        payload = json.loads(sys.stdin.read())
    except json.JSONDecodeError as exc:
        fail(f"ship payload is not valid JSON: {exc}")
    if not isinstance(payload, dict):
        fail("ship payload must be a JSON object")
    missing = [k for k in REQUIRED_KEYS if k not in payload]
    if missing:
        fail(f"ship payload missing required key(s): {', '.join(missing)}")
    payload.setdefault("language", _default_language())
    sys.stdout.write(render(payload))


if __name__ == "__main__":
    main()
