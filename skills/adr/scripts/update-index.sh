#!/bin/zsh
# ADR Index Update Script
# Usage: update-index.sh [adr-directory]
# Output (stdout): path to updated index file
# Errors (stderr): directory not found etc. with exit 1

set -euo pipefail

if [ -n "${1:-}" ]; then
  ADR_DIR="$1"
elif [ -d "adr" ]; then
  ADR_DIR="adr"
else
  ADR_DIR="docs/adr"
fi

if [ ! -d "$ADR_DIR" ]; then
  echo "Error: directory not found: $ADR_DIR" >&2
  exit 1
fi

INDEX_FILE="$ADR_DIR/README.md"

TEMP_FILE=$(mktemp)
TEMP_PROPOSED=$(mktemp)
TEMP_ACCEPTED=$(mktemp)
TEMP_DEPRECATED=$(mktemp)
TEMP_SUPERSEDED=$(mktemp)

trap "rm -f $TEMP_FILE $TEMP_PROPOSED $TEMP_ACCEPTED $TEMP_DEPRECATED $TEMP_SUPERSEDED" EXIT

cat > "$TEMP_FILE" <<'EOF'
# Architecture Decision Records

This directory contains important decisions about the project's architecture.

## ADR List

| Number | Title | Status | Date |
|--------|-------|--------|------|
EOF

while IFS= read -r adr_file; do
  FILENAME=$(basename "$adr_file")
  NUMBER=$(echo "$FILENAME" | grep -oE '^[0-9]{4}')
  TITLE=$(grep -m 1 '^# ' "$adr_file" | sed 's/^# //' || true)
  STATUS=$(grep -m 1 '^- Status:' "$adr_file" | sed 's/^- Status: //' | tr -d ' ' || true)
  STATUS=${STATUS:-proposed}
  DATE=$(grep -m 1 '^- Date:' "$adr_file" | sed 's/^- Date: //' | tr -d ' ' || true)
  DATE=${DATE:-Not set}

  echo "| [$NUMBER]($FILENAME) | $TITLE | $STATUS | $DATE |" >> "$TEMP_FILE"

  case "$STATUS" in
    proposed)   echo "$NUMBER|$TITLE" >> "$TEMP_PROPOSED" ;;
    accepted)   echo "$NUMBER|$TITLE" >> "$TEMP_ACCEPTED" ;;
    deprecated) echo "$NUMBER|$TITLE" >> "$TEMP_DEPRECATED" ;;
    superseded) echo "$NUMBER|$TITLE" >> "$TEMP_SUPERSEDED" ;;
  esac
done < <(find "$ADR_DIR" -name "[0-9][0-9][0-9][0-9]-*.md" -type f | sort)

cat >> "$TEMP_FILE" <<'EOF'

## By Status

EOF

emit_status_section() {
  local heading="$1"
  local source_file="$2"
  if [ -s "$source_file" ]; then
    echo "### $heading" >> "$TEMP_FILE"
    echo "" >> "$TEMP_FILE"
    while IFS='|' read -r num title; do
      echo "- **$num**: $title" >> "$TEMP_FILE"
    done < <(sort "$source_file")
    echo "" >> "$TEMP_FILE"
  fi
}

emit_status_section "Proposed" "$TEMP_PROPOSED"
emit_status_section "Accepted" "$TEMP_ACCEPTED"
emit_status_section "Deprecated" "$TEMP_DEPRECATED"
emit_status_section "Superseded" "$TEMP_SUPERSEDED"

cat >> "$TEMP_FILE" <<'EOF'
## About MADR Format

This project uses [MADR (Markdown Architecture Decision Records)](https://adr.github.io/madr/) format.

### How to Create an ADR

```bash
/adr "Decision Title"
```

### Status Meanings

- **Proposed**: Awaiting review
- **Accepted**: Approved, implementing or completed
- **Deprecated**: Better alternative found
- **Superseded**: Replaced by another ADR

---

*Last updated: {{UPDATE_DATE}}*
*Auto-generated by: update-index.sh*
EOF

UPDATE_DATE=$(date +%Y-%m-%d)
sed -i.bak "s/{{UPDATE_DATE}}/$UPDATE_DATE/g" "$TEMP_FILE"
rm -f "$TEMP_FILE.bak"

mv "$TEMP_FILE" "$INDEX_FILE"

echo "$INDEX_FILE"
